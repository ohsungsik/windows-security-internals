/// test.executefrommemory.cpp
///
/// [목적]
/// 메모리에 기계어 코드를 직접 써넣고 실행할 수 있는지 테스트한다.
///
/// [배경 지식]
/// 우리가 작성한 C/C++ 코드는 컴파일러에 의해 "기계어"로 변환된다.
/// 기계어란 CPU가 직접 이해하는 바이트 배열이다.
/// 예를 들어 "return 42;" 라는 코드는 다음 6바이트로 변환된다:
///
///     B8 2A 00 00 00   ->  mov eax, 42   (eax 레지스터에 42를 넣어라)
///     C3               ->  ret           (호출자에게 돌아가라)
///
/// CPU는 메모리의 특정 주소를 가리키면 거기 있는 바이트를 명령어로 해석하고 실행한다.
/// 즉, 어떤 메모리든 위의 바이트를 써넣고 CPU가 그 주소로 점프하면 코드가 실행된다.
///
/// [보안 문제]
/// 공격자가 프로그램의 데이터 버퍼에 악성 기계어를 써넣고,
/// 프로그램의 실행 흐름을 그 주소로 돌리면 악성 코드가 실행된다.
/// 이를 막기 위해 Windows는 DEP(Data Execution Prevention)를 도입했다.
/// DEP는 "데이터용 메모리에서는 코드 실행을 금지"하는 보호 기능이다.
///
/// [이 테스트의 흐름]
/// 1단계: 메모리를 할당하고 기계어 코드를 써넣는다.
/// 2단계: 실행 권한 없이 호출한다 -> DEP에 의해 차단되는지 확인한다.
/// 3단계: 실행 권한을 부여한 후 호출한다 -> 정상 실행되는지 확인한다.
///
/// \date 2026-04-23
/// \author oss<ohsungsik@outlook.com>

#include <Windows.h>
#include <cstdio>

int main()
{
    // 콘솔 출력 코드 페이지를 UTF-8로 설정한다.
    // 이렇게 하지 않으면 한글이 깨져서 출력된다.
    SetConsoleOutputCP(CP_UTF8);

    //=========================================================================
    // [1단계] 메모리를 할당하고, 기계어 코드를 써넣는다.
    //=========================================================================
    //
    // VirtualAlloc은 Windows에서 메모리를 할당하는 함수이다.
    // PAGE_READWRITE는 "읽기/쓰기는 되지만 실행은 안 됨"을 의미한다.
    // 즉, 이 메모리에 코드를 써넣을 수는 있지만 실행하면 에러가 난다.
    //
    const SIZE_T bufferSize = 4096;  // 4KB 할당 (넉넉하게)
    BYTE* buffer = (BYTE*)VirtualAlloc(
        NULL,                // 주소는 OS가 알아서 정한다
        bufferSize,          // 할당할 크기
        MEM_COMMIT | MEM_RESERVE,  // 메모리를 확보하고 사용 가능 상태로 만든다
        PAGE_READWRITE       // 읽기/쓰기만 허용, 실행은 불가
    );

    if (!buffer)
    {
        printf("메모리 할당 실패\n");
        return 1;
    }

    printf("메모리 할당 성공: 주소 = %p\n", buffer);

    //
    // 이제 이 메모리에 기계어 코드를 써넣는다.
    // 아래는 x64 기계어로 "42를 리턴하는 함수" 전체이다.
    //
    //   B8 2A 00 00 00   ->  mov eax, 0x2A   (eax에 42를 넣는다. 0x2A = 42)
    //   C3               ->  ret             (호출자에게 돌아간다)
    //
    // C 코드로 표현하면 이런 함수와 동일하다:
    //   int myFunc() { return 42; }
    //
    BYTE code[] = { 0xB8, 0x2A, 0x00, 0x00, 0x00, 0xC3 };

    //
    // 할당한 버퍼에 기계어 코드를 복사한다.
    // 이 시점에서 buffer에는 CPU가 실행할 수 있는 명령어가 들어있다.
    // 하지만 아직 실행 권한이 없으므로 실행하면 에러가 난다.
    //
    memcpy(buffer, code, sizeof(code));

    //
    // buffer를 "인자 없이 int를 리턴하는 함수 포인터"로 캐스팅한다.
    // 이렇게 하면 func()를 호출할 때 CPU가 buffer 주소로 점프한다.
    //
    typedef int (*FuncPtr)();
    FuncPtr func = (FuncPtr)buffer;

    //=========================================================================
    // [2단계] 실행 권한 없이 호출한다 -> DEP가 차단하는지 확인
    //=========================================================================
    //
    // 현재 buffer는 PAGE_READWRITE(읽기/쓰기 전용)이다.
    // 여기서 코드를 실행하면 DEP가 이를 감지하고 프로그램을 중단시킨다.
    //
    // __try/__except는 Windows의 예외 처리 구문이다.
    // DEP 위반 시 발생하는 Access Violation 예외를 잡아서
    // 프로그램이 죽지 않고 메시지를 출력할 수 있게 한다.
    //
    printf("\n--- 실행 권한 없이 호출 시도 ---\n");

    __try
    {
        int result = func();
        //
        // 만약 여기까지 왔다면 DEP가 동작하지 않은 것이다.
        // 정상적인 Windows 환경에서는 여기에 도달하지 않는다.
        //
        printf("실행 성공?! 리턴값 = %d (DEP가 비활성화된 환경)\n", result);
    }
    __except (EXCEPTION_EXECUTE_HANDLER)
    {
        //
        // 여기로 오면 DEP가 정상적으로 실행을 차단한 것이다.
        // "데이터 메모리에서 코드를 실행하려 했으므로 차단했다"는 의미.
        //
        printf("예외 발생! DEP가 실행을 차단했습니다.\n");
        printf("-> 읽기/쓰기 전용 메모리에서는 코드를 실행할 수 없습니다.\n");
    }

    //=========================================================================
    // [3단계] 실행 권한을 부여한 후 호출한다 -> 정상 실행 확인
    //=========================================================================
    //
    // VirtualProtect로 메모리의 보호 속성을 변경한다.
    // PAGE_EXECUTE_READ는 "읽기 + 실행 허용"을 의미한다.
    // 이렇게 하면 DEP가 더 이상 실행을 차단하지 않는다.
    //
    // 이것이 보안 취약점의 핵심이다:
    // 공격자가 VirtualProtect 호출을 유도할 수 있으면
    // 데이터 메모리에 넣어둔 악성 코드를 실행할 수 있게 된다.
    //
    printf("\n--- 실행 권한 부여 후 호출 시도 ---\n");

    DWORD oldProtect;
    BOOL ok = VirtualProtect(
        buffer,              // 대상 메모리 주소
        bufferSize,          // 크기
        PAGE_EXECUTE_READ,   // 새 보호 속성: 읽기 + 실행 허용
        &oldProtect          // 이전 보호 속성이 여기에 저장된다
    );

    if (!ok)
    {
        printf("VirtualProtect 실패\n");
        VirtualFree(buffer, 0, MEM_RELEASE);
        return 1;
    }

    printf("메모리 보호 속성 변경 완료: PAGE_READWRITE -> PAGE_EXECUTE_READ\n");

    //
    // 이제 같은 주소를 다시 호출한다.
    // 실행 권한이 부여되었으므로 DEP가 차단하지 않고 코드가 실행된다.
    //
    int result = func();

    printf("실행 성공! 리턴값 = %d\n", result);

    if (result == 42)
        printf("-> 메모리에 써넣은 기계어 코드가 정상적으로 실행되었습니다.\n");

    //=========================================================================
    // [정리] 할당한 메모리를 해제한다.
    //=========================================================================
    VirtualFree(buffer, 0, MEM_RELEASE);

    return 0;
}
